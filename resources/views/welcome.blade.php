    <script>
        $(document).on('click', '#send-message', function() {
            $('.name_err').text('');
            $('.email_err').text('');
            $('.message_err').text('');

            $.ajax({
                type: 'post',
                url: "{{ route('contact.email') }}",
                data: {
                    'name': $('#name').val(),
                    'email': $('#email').val(),
                    'message': $('#message').val(),
                    "_token": "{{ csrf_token() }}",
                },
                success: function(data) {
                    printMsg(data);
                },
                error: function(data) {

                },
            });
        });

        function printMsg(msg) {
            if ($.isEmptyObject(msg.error)) {
                $('#name').val('');
                $('#email').val('');
                $('#message').val('');
                alert('successfully send mail');

            } else {
                $.each(msg.error, function(key, value) {
                    $('.' + key + '_err').text(value);
                });
            }
        }
    </script>


